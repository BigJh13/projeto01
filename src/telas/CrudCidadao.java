    private void btnEditarCidadaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarCidadaoMouseClicked
        String nome = txtNomeCidadao.getText();
        String endereco = txtEnderecoCidadao.getText();
        String dataNascimento = txtfDataNascimentoCidadao.getText();
        String email = txtEmailCidadao.getText();
        String celular = txtfCelularCidadao.getText();
        String associado_saude = ComboBox3.getSelectedItem().toString();
        String nomeVacina = cbVacinaCidadao.getSelectedItem().toString();
        String dosesTomadas = ComboBox1.getSelectedItem().toString();
        String cpf = txtfCpfCidadao.getText();
        String dataVacinacao = txtfDataVacinacao.getText();

        if (cpf.equals("")) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar", "Informe o cpf", JOptionPane.ERROR_MESSAGE);
        } else {
            Cidadao cidadao = new Cidadao();
            cidadao.setNome(nome);
            cidadao.setEndereco(endereco);
            cidadao.setDataNascimento(dataNascimento);
            cidadao.setEmail(email);
            cidadao.setCelular(celular);
            cidadao.setAssociado_saude(associado_saude);
            cidadao.setNomeVacina(nomeVacina);
            cidadao.setDosesTomadas(dosesTomadas);
            cidadao.setDataVacinacao(dataVacinacao);
            cidadao.setCpf(cpf);

            //alterando os dados do Cidadao
            cidadao.alterar();

            //limpando os campos
            txtNomeCidadao.setText("");
            txtEnderecoCidadao.setText("");
            txtfDataNascimentoCidadao.setText("");
            txtEmailCidadao.setText("");
            txtfCelularCidadao.setText("");
            txtfCpfCidadao.setText("");
            ComboBox3.setSelectedItem("Selecione");
            cbVacinaCidadao.setSelectedItem("Selecione");
            txtfDataVacinacao.setText("");
            ComboBox1.setSelectedItem("Selecione");
            txtfDataVacinacao.setText("");
        }
    }//GEN-LAST:event_btnEditarCidadaoMouseClicked

    private void carregaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        //arrumando layout da tabela
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(65);
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(65);
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(65);
        //aumenta o tamanho entre as linhas
        jTable1.setRowHeight(25);
        
        // jTable1.getColumnModel().getColumn(0).setMaxWidth(80);
        //jTable1.getColumnModel().getColumn(1).setMaxWidth(80);
        //jTable1.getColumnModel().getColumn(2).setMaxWidth(80);
        //Cidadao cidadao = new Cidadao();

        String sql = "SELECT nome, email, celular FROM Cidadao";

        ConnectionFactory factory = new ConnectionFactory();
        try ( Connection c = factory.obtemConexao()) {
            PreparedStatement ps = c.prepareStatement(sql);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getString(1),
                    rs.getString(2),
                    rs.getString(3),});

            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro com banco de dados");
        }
    }

